# Mathematicl operations.
# PEMDAS stands for Parentheses, Exponents, Multiplication and Division (from left to right), Addition and Subtraction (from left to right).
# It is the order of operations in mathematics that determines the sequence in which calculations are performed.
# In Python, the order of operations follows PEMDAS as well.
print("my age " + str(19) + " years old")
print(123 + 456)  # Addition
print(985 - 745)  # Subtraction
print(12 / 3)  # Division
print(5 // 2) # Floor Division, it removes the decimel point.
print(2**4)  # Exponentiation, 2 raised to the power of 4.