# It generates a band name based on the user's input for their city and pet's name.
print("Welcome to the Band Name Generator!")
city = input("What's the name of the city you grew up in?\n")
pet = input("What's the name of your pet?\n")
band_name = city + " " + pet
print("Your band name could be: " + band_name)
# This code takes the user's input for their city and pet's name,
# and combines them to create a band name.